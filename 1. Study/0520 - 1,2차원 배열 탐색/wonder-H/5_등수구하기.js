// N = 3~1000 , score = n[index],  
function getResult(arr) {
  const score = arr.length;
  const result = Array(score).fill(1);
  for (let i = 0; i < score; i += 1) {
    for (let j = 0; j < score; j += 1) {
      if (arr[i] < arr[j]) {
        result[i] += 1;
      }
    }
  }
  return result;
}

const arr = [87, 89, 92, 100, 76];
console.log(getResult(arr));